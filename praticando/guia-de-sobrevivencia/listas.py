name_list = ['Alice', 'Bob', 'Charlie', 'David']
print(name_list)
print(name_list[0])  # Acessa o primeiro elemento
print(len(name_list))  # Imprime o tamanho da lista
print(name_list[-1])  # Acessa o último elemento
print(name_list[1:3])  # Fatiamento da lista, ou seja ele esta pegando do segundo ao terceiro elemento
name_list.append('Eve')  # Adiciona um novo elemento
name_list.remove('Charlie')  # Remove um elemento
name_list.sort()  # Ordena a lista
name_list.reverse()  # Inverte a ordem da lista
name_list.pop(2)  # Remove um elemento na posição especificada
name_list.clear()  # Limpa a lista
name_list.insert(0, 'Zara')  # Insere um elemento na posição especificada
